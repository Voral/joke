<?php

declare(strict_types=1);

namespace Vasoft\Joke\Http\Response\Html;

/**
 * Коллекция уникальных строк для генерации HTML-блоков.
 *
 * Предназначена для хранения и сборки произвольных фрагментов HTML-кода,
 * таких как инлайн-скрипты (<script>...</script>), стили (<style>...</style>)
 * или другие повторяющиеся элементы, требующие автоматической дедупликации.
 *
 * Уникальность элементов определяется полным содержимым строки с использованием
 * хеш-функции MD5. Это гарантирует, что идентичные блоки кода не будут продублированы
 * в финальной разметке, даже если метод add() был вызван несколько раз.
 */
class StringCollection
{
    /**
     * @var array<string, string> ассоциативный массив элементов, где ключ — MD5-хеш строки
     */
    private array $elements = [];

    /**
     * Инициализирует коллекцию с заданным разделителем.
     *
     * @param string $separator строка, используемая для соединения элементов при сборке (например, "\n" или "")
     */
    public function __construct(
        private readonly string $separator,
    ) {}

    /**
     * Добавляет строку в коллекцию.
     *
     * Если идентичная строка уже существует в коллекции (определяется по MD5-хешу),
     * добавление игнорируется для предотвращения дублирования контента.
     *
     * @param string $element строковое значение для добавления (HTML-код, скрипт, стиль)
     */
    public function add(string $element): static
    {
        $hash = md5($element);
        $this->elements[$hash] = $element;

        return $this;
    }

    /**
     * Собирает все уникальные элементы в единую строку.
     *
     * Элементы объединяются с использованием разделителя, указанного в конструкторе.
     * Порядок элементов соответствует порядку их первого добавления.
     *
     * @return string результирующая строка, содержащая все уникальные элементы
     */
    public function build(): string
    {
        return implode($this->separator, array_values($this->elements));
    }
}

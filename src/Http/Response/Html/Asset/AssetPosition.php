<?php

declare(strict_types=1);

namespace Vasoft\Joke\Http\Response\Html\Asset;

/**
 * Перечисление позиций размещения ресурсов (ассетов) в HTML-документе.
 *
 * Используется для строгой типизации места подключения внешних файлов
 * (CSS, JavaScript) при сборке страницы через классы Asset и AssetCollection.
 *
 * Гарантирует, что ресурс будет добавлен только в допустимую секцию документа:
 * - HEAD: Внутрь тега <head> (например, таблицы стилей, мета-теги, критический JS).
 * - BODY: Перед закрывающим тегом </body> (например, скрипты инициализации, аналитика).
 */
enum AssetPosition: int
{
    /**
     * Позиция в секции <head>.
     * Рекомендуется для CSS и критических ресурсов, блокирующих рендеринг.
     */
    case HEAD = 1;
    /**
     * Позиция в конце секции <body> (перед </body>).
     * Рекомендуется для JavaScript, чтобы не блокировать отрисовку страницы.
     */
    case BODY = 2;
}
